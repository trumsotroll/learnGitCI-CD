/**
 *  Mã hóa ARM_GPIO_Pin_t
 * + bit (0 - 4): Pin ID, mô tả dãy giá trị [0 - 31]
 * + bit (5 - 7): Port ID, mô tả dãy giá trị [0 - 7]
 * vì coder sử dụng macro đã định sẵn nên không cần xử lý nhập PortID [5 - 7]
 */
